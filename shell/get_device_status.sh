ip=$(ifconfig | grep "inet addr" | grep -v 127.0.0.1 | awk '{print $2}' | awk -F: '{print $2}')
echo $ip
echo "#########################################" >> $ip.txt
echo "########## "$(date)" ##########" >> $ip.txt
echo "#########################################" >> $ip.txt
echo "------------------------------> Run Command: df -h" >> $ip.txt
df -h >> $ip.txt
echo -e "------------------------------------------------------------------------------------------\n\n" >> $ip.txt
echo "------------------------------> Run Command: top -n 1"  >> $ip.txt
top -b -n 1 >> $ip.txt
echo -e "------------------------------------------------------------------------------------------\n\n" >> $ip.txt
echo "------------------------------> Run Command: free" >> $ip.txt
free >> $ip.txt
echo -e "------------------------------------------------------------------------------------------\n\n" >> $ip.txt
echo "------------------------------> Run Command: ps" >> $ip.txt
ps >> $ip.txt
echo -e "------------------------------------------------------------------------------------------\n\n" >> $ip.txt
echo "------------------------------> Run Command: uptime" >> $ip.txt
uptime >> $ip.txt
echo -e "------------------------------------------------------------------------------------------\n\n" >> $ip.txt